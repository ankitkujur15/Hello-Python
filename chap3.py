
# data types in python

"""
  * You don't need to declare  the data type -- python automatically assignes the data type based on the value you assigne to the variable.


"""
#int
a = -34
print(type(a))   # <class 'int'>

#float
sher = 3.5
print(type(sher))   # <class 'float'>

#complex
c = 12 + 3j
print(type(c))   # <class 'complex'>   j -> represents the imaginary part of a complex number

#string
str = 'sherians is good for "frontend" development'
print(str)         # <class 'string'>

#boolean

a = True
b = False

print(type(a))   # <class 'bool'>
print(type(b))   # <class 'bool'>


# class type() is used to check the data type of a  variable.